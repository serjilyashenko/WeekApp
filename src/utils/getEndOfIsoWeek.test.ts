import { getEndOfIsoWeek } from './getEndOfIsoWeek';

it.each([
  ['2023-07-22T00:00:00', '2023-07-23, 23:59:59'],
  ['2022-12-26T15:00:00', '2023-01-01, 23:59:59'],
  ['2023-01-03T01:00:00', '2023-01-08, 23:59:59'],
  ['2023-03-10T01:00:00', '2023-03-12, 23:59:59'],
  ['2023-02-01T01:00:00', '2023-02-05, 23:59:59'],
  ['2023-04-01T12:00:00', '2023-04-02, 23:59:59'],
  ['2023-05-15T00:00:00', '2023-05-21, 23:59:59'],
  ['2023-06-20T08:00:00', '2023-06-25, 23:59:59'],
  ['2023-08-01T12:00:00', '2023-08-06, 23:59:59'],
  ['2023-09-05T18:30:00', '2023-09-10, 23:59:59'],
  ['2023-10-10T09:15:00', '2023-10-15, 23:59:59'],
  ['2023-11-21T03:45:00', '2023-11-26, 23:59:59'],
  ['2024-01-02T00:00:00', '2024-01-07, 23:59:59'],
  ['2024-02-27T15:30:00', '2024-03-03, 23:59:59'],
  ['2024-04-15T06:45:00', '2024-04-21, 23:59:59'],
  ['2024-05-21T22:00:00', '2024-05-26, 23:59:59'],
  ['2024-07-01T12:00:00', '2024-07-07, 23:59:59'],
  ['2024-08-15T19:30:00', '2024-08-18, 23:59:59'],
  ['2024-09-10T10:15:00', '2024-09-15, 23:59:59'],
  ['2024-10-21T04:45:00', '2024-10-27, 23:59:59'],
  ['2024-11-26T23:59:59', '2024-12-01, 23:59:59'],
])('test: %o => %o', (input, output) => {
  const date = new Date(input);
  expect(getEndOfIsoWeek(date).toLocaleString('en-SE')).toBe(output);
});
